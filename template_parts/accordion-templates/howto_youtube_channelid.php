To get your channel ID:<br>
- Sign in to YouTube.<br>
- In the top right, click your profile picture.<br>
- Select Settings from the dropdown menu.<br>
- Select Advanced settings from the menu on the left.<br>
- You’ll see your channel’s user and channel IDs.<br>
- Copy the channel ID to the plugin settings.<br><br>
If you have trouble finding it, refer to Google's own documentation:<br><a href="https://support.google.com/youtube/answer/3250431?hl=en" title="">https://support.google.com/youtube/answer/3250431?hl=en</a><br>